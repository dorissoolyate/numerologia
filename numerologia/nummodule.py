def analyze_numbers(numbers):
    # Примерный анализ чисел
    analysis_results = {
        1: "Число 1: Лидерство, независимость, инновации.",
        2: "Число 2: Дипломатия, взаимодействие, партнёрство.",
        3: "Число 3: Творчество, самовыражение, общение.",
        4: "Число 4: Безопасность, честность, практичность",
        5: "Число 5: Дружелюбие, стремление к удовольствиям, вспыльчивость",
        6: "Число 6: Стремление к справедливости, чувство прекрасного, логика",
        7: "Число 7: Непостоянство, оккультизм, молчаливость",
        8: "Число 8: Щедрость, несдержанность",
        9: "Число 9: Любовь, расточительность"
    }
    results = []
    for number in numbers:
        if number in analysis_results:
            results.append(analysis_results[number])
        else:
            results.append(f"Число {number}: Значение не найдено.")
    return " ".join(results)

